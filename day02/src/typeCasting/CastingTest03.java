package typeCasting;

public class CastingTest03 {
	public static void main(String[] args) {
		//유니코드 값 확인
		char ch1 = 'A';
		char ch2 = 'a';
		System.out.println(ch1);
		System.out.println(ch2);
		char ch3 = 65; //'A'
		char ch4 = 97; //'a'
		System.out.println(ch3);
		System.out.println(ch4);
		
	}
}
